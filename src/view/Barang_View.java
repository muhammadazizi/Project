    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            barang b = new barang();
            b.setNama_barang(tfNamaBarang.getText());
            b.setJenis_barang(controller.getDataJenisBarang().get(cbJenisBarang.getSelectedIndex()));
            b.setHarga_barang(Integer.parseInt(tfHarga.getText()));
            b.setUkuran_barang(Integer.parseInt(tfUkuran.getText()));
            
            controller.insertBarang(b);
            showtblBarang();
       } catch (SQLException ex) {
            Logger.getLogger(Barang_View.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            barang b = controller.getDataBarang().get(tblBarang.getSelectedRow());
            int Harga_barang = Integer.parseInt(tfHarga2.getText());
            int Ukuran_barang = Integer.parseInt(tfUkuran2.getText());
            
            controller.updateBarang(b, Harga_barang, Ukuran_barang);
            showtblBarang();
        } catch (SQLException ex) {
            Logger.getLogger(Barang_View.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            barang barang = controller.getDataBarang().get(tblBarang.getSelectedRow());
            controller.deleteBarang(barang);
            showtblBarang();
        } catch (SQLException ex) {
            Logger.getLogger(Barang_View.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

