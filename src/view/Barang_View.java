    public void cbJenisBarang() throws SQLException {
        DefaultComboBoxModel cbj = new DefaultComboBoxModel();
        for(jenis_barang j : controller.getDataJenisBarang()){
            cbj.addElement(j.getKualitas_jenis_barang());
        }
        cbJenisBarang.setModel(cbj);
    }
    public void showtblBarang() throws SQLException {
        DefaultTableModel dtmBarang = new DefaultTableModel(new String[]{"Nama_Barang", "Harga", "Ukuran"},0);
        
        for(barang b : controller.getDataBarang()) {
            dtmBarang.addRow(new String[]{b.getNama_barang().toString(), b.getHarga_barang().toString(), b.getUkuran_barang().toString()});
        }
        tblBarang.setModel(dtmBarang);
    }

